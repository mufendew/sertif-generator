const ubahRomawi = (angka) => {
    if (angka===1) {
        return 'I'   
    }else if(angka===2){
        return 'II'
    }else if(angka===3){
        return 'III'
    }else if(angka===4){
        return 'IV'
    }else if(angka===5){
        return 'V'
    }else if(angka===6){
        return 'VI'
    }else if(angka===7){
        return 'VII'
    }else if(angka===8){
        return 'VIII'
    }else if(angka===9){
        return 'VI'
    }else if(angka===10){
        return 'X'
    }else if(angka===11){
        return 'XI'
    }else if(angka===12){
        return 'XII'
    }

}

module.exports = ubahRomawi